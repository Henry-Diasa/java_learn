package com.study.basic;

public class FloatDemo {

	public static void main(String[] args) {
		float f = 1234.324f;
		double d = 123.897; // 默认double	
		double d1 = 123l;
		double d2 = d1;
		
		System.out.println("f=" + f);
		System.out.println("d=" + d);
		System.out.println("d1=" + d1);
		System.out.println("d2=" + d2);
	}

}
